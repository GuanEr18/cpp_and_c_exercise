#include <stdio.h>
int main() {
    int i;
    int a[3],b[3],c[3];
    for (i=123;i<333;i++) {
        a[0]=i%10;
        a[1]=i/10%10;
        a[2]=i/100;
        b[0]=2*i%10;
        b[1]=2*i/10%10;
        b[2]=2*i/100;
        c[0]=3*i%10;
        c[1]=3*i/10%10;
        c[2]=3*i/100;
        if(a[0]+a[1]+a[2]+b[0]+b[1]+b[2]+c[0]+c[1]+c[2]==45
        && a[0]*a[1]*a[2]*b[0]*b[1]*b[2]*c[0]*c[1]*c[2]==362880) {
            printf("%d %d %d\n",i,2*i,3*i);
        }
    }
    return 0;
}